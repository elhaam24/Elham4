public class coffeeshop {
    int coffeeshopId;
    String coffeeshopName;

    public coffeeshop(int Id, String Name) {
        coffeeshopId = Id;
        coffeeshopName = Name;
    }

    public int getCoffeeshopId() {
        return coffeeshopId;
    }

    public String getCoffeeshopName() {
        return coffeeshopName;
    }
}
